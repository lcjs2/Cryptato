uint8_t sbox(uint8_t x) {return s_forward[x];}
uint8_t inv_sbox(uint8_t x) {return s_inverse[x];}

// Perform "key schedule core" on t
void schedule_core(uint8_t* t, int i)
{
	uint8_t temp = t[0];

	t[0]=sbox(t[1])^rcon[i];
	t[1]=sbox(t[2]);
	t[2]=sbox(t[3]);
	t[3]=sbox(temp);
}

// Accepts keys of 128, 192 or 256 bits (16, 24 or 32 bytes)
// Produces 176=11x16 bytes
void key_schedule(uint8_t* result, const uint8_t* key, int key_bytes)
{
	int i=1;
	uint8_t t[4];
	
	// First n bytes of the key schedule are the key itself
	memcpy(result, key, key_bytes);
	result+=key_bytes;
	int bytes_made=key_bytes;
	while(bytes_made<176)
	{
		for(int j=0;j<4;j++)
		{
			memcpy(t, result-4,4);
			if(j==0) schedule_core(t,i++);
			for(int k=0;k<4;k++) result[k]=t[k]^(result[k-key_bytes]);
			result+=4;
			bytes_made+=4;
		}
	}
	return;
}

// XOR the 16 state bytes with 16 round key bytes
void xor_key(uint8_t* state, uint8_t* keys)
{
	for(int i=0;i<16;i++) state[i]^=keys[i];
	return;
}

// Run each byte in the state through the S-box
void sub_bytes(uint8_t* state)
{
	for(int i=0;i<16;i++) state[i]=sbox(state[i]);
	return;
}

void inv_sub_bytes(uint8_t* state)
{
	for(int i=0;i<16;i++) state[i]=inv_sbox(state[i]);
	return;
}

// Shift row n left by n bytes
void shift_rows(uint8_t* state)
{
	uint8_t temp;
	// Second row left one
	temp=state[1];
	state[1]=state[5]; state[5]=state[9];
	state[9]=state[13]; state[13]=temp;
	// Third row left two
	temp=state[2];
	state[2]=state[10]; state[10]=temp;
	temp=state[6];
	state[6]=state[14]; state[14]=temp;
	// Fourth row left three
	temp=state[3];
	state[3]=state[15]; state[15]=state[11];
	state[11]=state[7]; state[7]=temp;
	
	return;
}

void inv_shift_rows(uint8_t* state)
{
	uint8_t temp;
	// Second row right one
	temp=state[1];
	state[1]=state[13]; state[13]=state[9];
	state[9]=state[5]; state[5]=temp;
	// Third row right two
	temp=state[2];
	state[2]=state[10]; state[10]=temp;
	temp=state[6];
	state[6]=state[14]; state[14]=temp;
	// Fourth row right three
	temp=state[3];
	state[3]=state[7]; state[7]=state[11];
	state[11]=state[15]; state[15]=temp;
	
	return;
}

// Multiplication in the field of polynomials over Z/2Z
// mod x^8 = x^4+x^3+x+1, where each byte is a list of coefficients.
uint8_t gal_multiply(uint8_t a, uint8_t b)
{
	// Multiplying by 2, 4 or 8 (i.e. powers of x) is a shift
	// plus a value depending on the final 1, 2 or 3 bits
	uint8_t wraparound[8] = {0, 0x1b, 0x36, 0x1b^0x36,
		0x6c, 0x6c^0x1b, 0x6c^0x36, 0x6c^0x36^0x1b};

	if(b==1) return a;
	if(b==2) return (a<<1)^wraparound[a>>7];
	if(b==4) return (a<<2)^wraparound[a>>6];
	if(b==8) return (a<<3)^wraparound[a>>5];
	if(b>8) return gal_multiply(a,8) ^ gal_multiply(a,b-8);
	if(b>4) return gal_multiply(a,4) ^ gal_multiply(a,b-4);
	if(b>2) return gal_multiply(a,2) ^ gal_multiply(a,b-2);
	return 0;
}

// Apply the mix columns operation to one column of state
void multiply_column(uint8_t* top, uint8_t* matrix)
{
	uint8_t c[4];
	for(int i=0;i<4;i++) c[i]=top[i];

	for(int i=0;i<4;i++)
	{
		top[i]=0;
		for(int j=0;j<4;j++)
		{
			top[i] ^= gal_multiply(c[j], matrix[4*i + j]);
		}
	}
	return;
}

void mix_columns(uint8_t* state)
{
	uint8_t matrix[16] = {
		2, 3, 1, 1,
		1, 2, 3, 1,
		1, 1, 2, 3,
		3, 1, 1, 2};
	for(int i=0;i<4;i++) multiply_column(state+(4*i), matrix);
	return;
}

void inv_mix_columns(uint8_t* state)
{
	uint8_t matrix[16] = {
		14, 11, 13, 9,
		9,  14, 11, 13,
		13, 9,  14, 11,
		11, 13, 9,  14};
	for(int i=0;i<4;i++) multiply_column(state+(4*i), matrix);
	return;
}

void aes_encode_block(uint8_t* result, const uint8_t* data,
		const uint8_t*key, int key_size)
{
	uint8_t state[16];
	memcpy(state, data, 16);

	uint8_t round_keys[176];
	key_schedule(round_keys, key, 16);

	// Begin rounds
	xor_key(state, round_keys);
	for(int i=0;i<10;i++)
	{
		sub_bytes(state);
		shift_rows(state);
		if(i!=10-1) mix_columns(state);
		xor_key(state, round_keys + 16 + 16*i);
	}

	// Output
	memcpy(result, state, 16);
	return;
}

void aes_decode_block(uint8_t* result, const uint8_t* data,
		const uint8_t*key, int key_size)
{
	uint8_t state[16];
	memcpy(state, data, 16);

	uint8_t round_keys[176];
	key_schedule(round_keys, key, 16);

	// Begin rounds
	for(int i=9;i>=0;i--)
	{
		xor_key(state, round_keys + 16 + 16*i);
		if(i!=10-1) inv_mix_columns(state);
		inv_shift_rows(state);
		inv_sub_bytes(state);
	}
	xor_key(state, round_keys);
	
	// Output
	memcpy(result, state, 16);
	return;
}
	
